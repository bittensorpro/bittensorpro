  /* =========================================================================
     ENGINE
     ========================================================================= */
  let _lang = localStorage.getItem('btp_lang') || 'en';

  function _get(key) {
    return (T[_lang] && T[_lang][key]) || (T['es'] && T['es'][key]) || key;
  }

  window.t        = _get;
  window.getLang  = () => _lang;
  window.setLang  = function (lang) {
    if (!T[lang]) return;
    _lang = lang;
    localStorage.setItem('btp_lang', lang);
    document.documentElement.lang = lang;
    _applyStatic();
    document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
    _updateSwitcher();
  };

  function _applyStatic() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      const val = _get(key);
      if (el.tagName === 'INPUT' && el.type !== 'submit') {
        el.placeholder = val;
      } else {
        el.textContent = val;
      }
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.dataset.i18nHtml;
      const val = _get(key);
      if (val && val !== key) el.innerHTML = val;
    });
  }

  /* =========================================================================
     SWITCHER UI
     ========================================================================= */
  const _SWITCHER_CSS = `
    .btp-lang {
      display: inline-flex;
      gap: 2px;
      background: rgba(0,240,255,.05);
      border: 1px solid rgba(0,240,255,.18);
      border-radius: 6px;
      padding: 2px;
      flex-shrink: 0;
    }
    .btp-lang button {
      background: transparent;
      border: none;
      color: var(--text-dim, #8a95a8);
      font-family: 'Share Tech Mono', 'Courier New', monospace;
      font-size: 10px;
      font-weight: 700;
      letter-spacing: .1em;
      padding: 4px 8px;
      border-radius: 4px;
      cursor: pointer;
      transition: all .15s;
      line-height: 1;
    }
    .btp-lang button.active {
      background: rgba(0,240,255,.14);
      color: var(--cyan, #00f0ff);
      text-shadow: 0 0 6px rgba(0,240,255,.5);
    }
    .btp-lang button:hover:not(.active) {
      color: var(--text, #e8f6ff);
    }
  `;

  function _buildSwitcher() {
    const wrap = document.createElement('div');
    wrap.className = 'btp-lang';
    wrap.innerHTML = ['en', 'es'].map(l =>
      `<button data-btp-lang="${l}"${l === _lang ? ' class="active"' : ''}>${l.toUpperCase()}</button>`
    ).join('');
    wrap.addEventListener('click', e => {
      const btn = e.target.closest('[data-btp-lang]');
      if (btn) window.setLang(btn.dataset.btpLang);
    });
    return wrap;
  }

  function _updateSwitcher() {
    document.querySelectorAll('[data-btp-lang]').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.btpLang === _lang);
    });
  }

  function _injectSwitcher() {
    try {
      const style = document.createElement('style');
      style.textContent = _SWITCHER_CSS;
      document.head.appendChild(style);

      // Slot declarativo tiene prioridad: funciona dentro y fuera del topbar
      const slot = document.querySelector('.btp-lang-slot');
      if (slot) {
        if (!slot.querySelector('.btp-lang')) slot.appendChild(_buildSwitcher());
        return;
      }

      const topbar = document.querySelector('.topbar') || document.querySelector('header');
      if (!topbar) {
        if (document.querySelector('.btp-lang')) return;
        const wrap = document.createElement('div');
        wrap.style.cssText = 'position:fixed;top:10px;right:14px;z-index:9999;';
        wrap.appendChild(_buildSwitcher());
        document.body.appendChild(wrap);
        return;
      }

      if (topbar.querySelector('.btp-lang')) return;

      const switcher = _buildSwitcher();
      const container = topbar.querySelector('.topbar-inner') || topbar;

      const directTaoInfo = [...container.children].find(el =>
        el.classList.contains('tao-info') || el.classList.contains('nav-tao')
      );

      if (directTaoInfo) {
        container.insertBefore(switcher, directTaoInfo);
      } else {
        const last = container.lastElementChild;
        if (last) container.insertBefore(switcher, last);
        else container.appendChild(switcher);
      }
    } catch(e) {
      console.warn('[i18n] switcher injection failed:', e);
    }
  }

  /* =========================================================================
     INIT
     ========================================================================= */
  function _init() {
    document.documentElement.lang = _lang;
    _injectSwitcher();
    _applyStatic();
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', _init);
  } else {
    _init();
  }
